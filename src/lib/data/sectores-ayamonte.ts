/**
 * Lista plana de sectores para los selectores del formulario y cobrador.
 */
export const NOMBRES_SECTORES: string[] = SECTORES_AYAMONTE.map(s => s.nombre);

/**
 * Devuelve el sector correspondiente a una calle dada (búsqueda aproximada).
 */
export function getSectorDeCalle(calle: string): string | null {
    if (!calle) return null;
    const calleNorm = calle.toLowerCase().trim();
    for (const sector of SECTORES_AYAMONTE) {
        const encontrada = sector.calles.some(c =>
            c.toLowerCase().includes(calleNorm) || calleNorm.includes(c.toLowerCase().split(',')[0].trim())
        );
        if (encontrada) return sector.nombre;
    }
    return null;
}
